ensimmainen = float(input('Anna ensimmainen kokonaisluku: '))
toinen = float(input('Anna toinen kokonaisluku: '))
kolmas = float(input('Anna kolmas kokonailuku: '))

summa = ensimmainen + toinen + kolmas
print(f'\nLukujen summa on: {summa}')

tulo = ensimmainen * toinen * kolmas
print(f'\nLukujen tulo on: {tulo}')

keskiarvo = (ensimmainen + toinen + kolmas) / 3
print(f'\nLukujen keskiarvo on: {keskiarvo}')
